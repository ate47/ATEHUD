	public static int getColor(Color color) {
		return getColorWithRGB(color.getRed(), color.getGreen(), color.getBlue());
	}
	public static int getColorWithRGB(int red, int green, int blue) {
		return blue + 256 * green + 256 * 256 * red;
	}
	public static int getRedGreen(double percentage) {
		double red = 255.0 - 170.0 * percentage;
		double green = 85.0 + 170.0 * percentage;
		double blue = 85.0 * Math.abs(percentage - 0.5);
		return getColorWithRGB((int) red, (int) green, (int) blue);
	}
	/**
	 * Get int color with Hex COlor
	 * 
	 * @param colorStr
	 *            HexColor (Example: #ff0000 is red)
	 * @return
	 */
	public static int hex2Rgb(String colorStr) {
		return getColorWithRGB(Integer.valueOf(colorStr.substring(1, 3), 16),
				Integer.valueOf(colorStr.substring(3, 5), 16), Integer.valueOf(colorStr.substring(5, 7), 16));
	}
